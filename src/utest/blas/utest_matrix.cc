  TEST( test_matrix_construct, basic ) {
    // -----------------------------------------------------------------------------------
    {
      Matrix A;
      EXPECT_EQ(0, size(A,0) );
      EXPECT_EQ(0, size(A,1) );
    }

    {
      Matrix A(4);
      EXPECT_EQ(4, size(A,0) );
      EXPECT_EQ(4, size(A,1) );
      EXPECT_TRUE( A.isSquare() );
    }

    {
      Matrix A(7,11);
      EXPECT_EQ(7, size(A,0) );
      EXPECT_EQ(11, size(A,1) );
      EXPECT_FALSE( A.isSquare() );
    }

    {
      Matrix A(7,11);
      EXPECT_EQ(7,  size(A,0) );
      EXPECT_EQ(11, size(A,1) );
      EXPECT_EQ(7,  *(A.M()) );
      EXPECT_EQ(11, *(A.N()) );
      EXPECT_EQ(7,  *(A.LDA()) );
    }
  }


